console.log('Vinculaciones');

//las variables se representa con la palabra "let"
let atrapado = 5 * 5;

console.log(atrapado)

console.log(atrapado + 10);

let diez = 10

//es una variable que contiene valor actual de 10, una variable despues de ser vinculada a algun valor también es una expresión que puede ser utilizada para realizar operaciones
console.log(diez * diez);
console.log(diez + diez);

//Las variable son datos que pueden cambiar su valor durante la ejecución del programa

let humor = 'ligero';

console.log(humor);

humor = 'oscuro';

console.log(humor);

//Un let puede realizar multiples vinculaciones
let uno = 1, dos = 2;
console.log(uno + dos);
//  resultado: 3

